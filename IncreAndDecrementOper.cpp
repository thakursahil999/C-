#include<iostream>
using namespace std;
int main()
{
    // *********Increment Operator**********
    int a=10;
    cout<<a++<<endl; //first the value print and than increment post increment
    cout<<a<<endl;

    cout<<++a<<endl;//first the value increment and than print pre increment
    cout<<a<<endl;

    // **********Decrement Operator***********

    int x=10;
    cout<<x--<<endl;
    cout<<x<<endl;

    cout<<--x<<endl;
    cout<<x;

}